return {
	{
		"stevearc/oil.nvim",
		dependencies = { "nvim-tree/nvim-web-devicons" },
		cmd = "Oil",
		keys = {
			{
				"<leader>e",
				function()
					local oil = require("oil")
					if require("oil").get_current_dir() then
						oil.close()
					else
						oil.open_float()
					end
				end,
				desc = "Toggle Oil Float",
			},
		},
		config = function()
			require("oil").setup({
				default_file_explorer = true,

				-- columns = {
				--  "icon",
				--  "size",
				--  "mtime",
				-- },
				columns = {
					"icon",
				},

				delete_to_trash = true,
				skip_confirm_for_simple_edits = true,
				prompt_save_on_select_new_entry = true,
				cleanup_delay_ms = 2000,

				constrain_cursor = "name",
				watch_for_changes = true,

				keymaps = {
					["<CR>"] = "actions.select",
					["sv"] = { "actions.select", opts = { vertical = true } },
					["sh"] = { "actions.select", opts = { horizontal = true } },
					["st"] = { "actions.select", opts = { tab = true } },

					["-"] = { "actions.parent", mode = "n" },
					["<BS>"] = { "actions.parent", mode = "n" }, -- Backspace para subir
					["g."] = { "actions.toggle_hidden", mode = "n" },
					["gs"] = { "actions.change_sort", mode = "n" },
					["gx"] = "actions.open_external",
					["q"] = { "actions.close", mode = "n" },
					["<C-q>"] = { "actions.close", mode = "n" }, -- cerrar tambi√©n con Ctrl+q
					["<C-l>"] = "actions.refresh",
				},

				use_default_keymaps = false,

				view_options = {
					show_hidden = true,
					natural_order = true,
					case_insensitive = true,
					sort = {
						{ "type", "asc" },
						{ "name", "asc" },
					},
					wrap = true,
				},

				float = {
					padding = 2,
					border = "rounded",
				},

				preview_win = {
					update_on_cursor_moved = true,
					preview_method = "fast_scratch",
				},
			})
		end,
	},
}
